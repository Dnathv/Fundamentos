# Objetivo: Uso de la estructura de repeticion "For in range"
# Problema: Definir de tipo entero la variable "Num", la cual 
#    tendra el valor inicial (Vi)de 1 y se incrementara en 1
#    hasta el valor final (Vf) de 10

print ("Uso de la estructura for in range\n")

Vi = 5; Vf = 20

for num in range (Vi,Vf):
    print ("Valor de num: ",num)
    
print("\nProceso terminado")
