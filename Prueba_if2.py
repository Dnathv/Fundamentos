# Problema: Usar expresiones logicas simples con un "if/else" para
#   saber si un numero es Par o Impar. 

X = int (input ("\nIntroduce el valor entero de X: "))
if ( X%2 == 0 ):
    print ("\n", X, "Es numero Par")
else:
    print ("\n", X, "Es numero Impar")

